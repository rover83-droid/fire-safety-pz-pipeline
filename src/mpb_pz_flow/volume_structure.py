def flattened_titles(sections: tuple[VolumeSection, ...]) -> list[str]:
    titles: list[str] = []
    for section in sections:
        titles.append(section.title)
        titles.extend(flattened_titles(section.subsections))
    return titles


def flattened_sections(sections: tuple[VolumeSection, ...]) -> list[VolumeSection]:
    result: list[VolumeSection] = []
    for section in sections:
        result.append(section)
        result.extend(flattened_sections(section.subsections))
    return result


def canonical_section_titles_for_fkp(fkp: str) -> list[str]:
    return flattened_titles(structure_for_fkp(fkp))
